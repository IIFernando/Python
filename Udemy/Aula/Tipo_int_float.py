'''
Tipos int e float
int - número inteiro
float - número com ponto flutuante
podendo ser negativo(-1) ou positivo(1)

A função type mostra o tipo que o python inferiu ao valor.
'''

print(1) #Número inteiro

print(type(1)) #Número com ponto flutuante

print(1.55) #Número com ponto flutuante

print(type(1.55)) #Número com ponto flutuante
